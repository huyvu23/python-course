
# This script demonstrates the use of the `math` module in Python.
import math

print(math.ceil(2.9))  # Round up to the nearest integer
print(math.floor(2.9))  # Round down to the nearest integer
print(math.factorial(5))  # Calculate the factorial of 5
print(math.gcd(12, 15))  # Calculate the greatest common divisor of 12 and 15
print(math.pi)  # Print the value of pi

x = 2.9

# Round the number to the nearest integer
print(round(x))


print(abs(-2.9))

