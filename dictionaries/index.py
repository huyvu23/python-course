# The code snippet you provided is creating a dictionary named `customer` with key-value pairs for a
# customer's information such as name, age, email, and phone number. The dictionary is then printed to
# display all the information stored in it.


customer = {
    "name": "John Doe",
    "age": 30,
    "email": "K8E0j@example.com",
    "phone": "123-456-7890"
}
customer["address"] = "123 Main St, Anytown, USA"

print(customer)
# Output: {'name': 'John Doe', 'age': 30, 'email': 'K8E0j@example.com', 'phone': '123-456-7890'}

print(customer["name"])
# Output: John Doe
print(customer["age"])
# Output: 30
print(customer["email"])
# Output: K8E0j@example.com
print(customer["phone"])
# Output: 123-456-7890